x = int(input("Taper le premier nombre: "))
y = int(input("Taper le deuxieme nombre: "))
z = int(input("Taper le troisieme nombre: "))
if z == x + y:
    print("La somme des deux premiers nombres est egale au troisieme nombre")
else:
    print("La somme des deux premiers nombres n'est pas egale au troisieme nombre")