n=int(input("Taper un chiffre: "))
if n==1:
    print("On est le Lundi")
elif n==2:
    print("On est le Mardi")
elif n==3:
    print("On est le Mercredi")
elif n==4:
    print("On est le Jeudi")
elif n==5:
    print("On est le Vendredi")
elif n==6:
    print("On est le Samedi")
elif n==7:
    print("On est le Dimanche")
else:
    print("chiffre invalide")
